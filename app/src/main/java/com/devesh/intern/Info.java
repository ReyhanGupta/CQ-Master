public class Info {

    String name;
    String genre;
    String story;
    String synopsis;
    String date;
    Integer time;

    public Info(String name, String genre, String story, String synopsis, String date, Integer time) {
        this.name = name;
        this.genre = genre;
        this.story = story;
        this.synopsis = synopsis;
        this.date = date;
        this.time = time;
    }



}
